package com.velociter.ems.pojo;

public class TasksPojo {
	
	private String taskID = null;
	private String task = null;
	private String taskDescription = null;
	private String taskStatus = null;
	private String taskAssignee = null;
	private String taskAssigner = null;
	private String taskCreationTime = null;
	private String taskUpdationTime = null;
	public String getTaskID() {
		return taskID;
	}
	public void setTaskID(String taskID) {
		this.taskID = taskID;
	}
	public String getTask() {
		return task;
	}
	public void setTask(String task) {
		this.task = task;
	}
	public String getTaskDescription() {
		return taskDescription;
	}
	public void setTaskDescription(String taskDescription) {
		this.taskDescription = taskDescription;
	}
	public String getTaskStatus() {
		return taskStatus;
	}
	public void setTaskStatus(String taskStatus) {
		this.taskStatus = taskStatus;
	}
	public String getTaskAssignee() {
		return taskAssignee;
	}
	public void setTaskAssignee(String taskAssignee) {
		this.taskAssignee = taskAssignee;
	}
	public String getTaskAssigner() {
		return taskAssigner;
	}
	public void setTaskAssigner(String taskAssigner) {
		this.taskAssigner = taskAssigner;
	}
	public String getTaskCreationTime() {
		return taskCreationTime;
	}
	public void setTaskCreationTime(String taskCreationTime) {
		this.taskCreationTime = taskCreationTime;
	}
	public String getTaskUpdationTime() {
		return taskUpdationTime;
	}
	public void setTaskUpdationTime(String taskUpdationTime) {
		this.taskUpdationTime = taskUpdationTime;
	}
	
	

}
